                    <p>Kabupaten Tegal berdiri pada tanggal 18 Mei 1601. Cikal bakal berdirinya Kabupaten Tegal tidak dapat dipisahkan dari sosok ketokohan Ki Gede Sebayu. Menurut silsilah, Ki Gede Sebayu merupakan keturunan trah Majapahit dari Batoro Katong atau Syech Sekar Delima (Adipati Wengker Ponorogo). Ayah Ki Gede Sebayu bernama Pangeran Onje (Adipati Purbalingga).</p>
                    <p>Sejak kecil, Ki Gede Sebayu diasuh oleh eyangnya yaitu Ki Ageng Wunut yang selama hidupnya diajari budi pekerti luhur. Hal ini membawa dampak bagi perkembangan Ki Gede Sebayu yang tumbuh menjadi anak yang berperilaku ramah dan santun. Setelah menginjak dewasa, Ki Gede Sebayu oleh ayahnya disuwitakan untuk menjadi prajurit di Keraton Pajang. Sebagai prajurit tamtama, Ki Gede Sebayu memperoleh pendidikan keprajuritan dan ilmu kanuragan. Ki Gede Sebayu mempunyai 2 orang anak yaitu Raden Ayu Rara Giyanti Subhaleksana dan Raden Mas Hanggawana.</p>
                    <p>Pada saat Arya Pangiri merebut tahta Pajang dari Pangeran Benowo. Ki Gede Sebayu pergi meninggalkan keraton Pajang menuju Desa Sedayu. Ki Gede Sebayu kemudian bergabung dengan prajurit Mataram bersama Pangeran Benowo untuk menyingkirkan Arya Pangiri.</p>
                    <p>Ketika itu Ki Gede Sebayu dengan tombak pendeknya menyerang prajurit Arya Pangiri, sehingga banyak yang tewas dan akhirnya Arya Pangiri menyerah dan diusir dari Keraton Pajang. Kemudian Keraton pajang diserahkan kepada Pangeran Benowo. Setelah selesai pertempuran (1587), Ki Gede Sebayu dan pengikutnya memutuskan untuk melakukan perjalanan ke arah barat dan sampai di Desa Taji, Bagelan disambut oleh Demung Ki Gede Karang Lo.</p>
                    <p>Melihat kesuburan tanahnya, Ki Gede Sebayu tergugah dan berniat bersama-sama penduduk meningkatkan hasil pertanian dengan memperluas lahan serta membuat saluran pengairan. Daerah yang sebagian besar merupakan tanah lading tersebut kemudian dinamakan Tegal.

Atas keberhasilannya, pada 18 Mei 1601 Panembahan Senopati mengangkat Ki Gede Sebayu menjadi Juru Demung (Penguasa Lokal di Tlatah Tegal) dengan pangkat Tumenggung setingkat Bupati. Peristiwa inilah yang merupakan berdirinya Kabupaten Tegal pada tanggal 18 Mei 1601.</p>
